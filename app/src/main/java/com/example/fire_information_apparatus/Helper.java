package com.example.fire_information_apparatus;

public class Helper {
    String value, value2;

    public Helper() {
    }

    public Helper(String value, String value2) {
        this.value = value;
        this.value2 = value2;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getValue2() {
        return value2;
    }

    public void setValue2(String value2) {
        this.value2 = value2;
    }
}
